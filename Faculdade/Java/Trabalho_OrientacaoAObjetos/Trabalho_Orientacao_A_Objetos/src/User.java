
public class User {
	private String name;
	private String idNumber;
	private String email;
	
	// E-mail related
	public void setEmail(String email) {
		this.email = email;
	}
	public String getEmail() {
		return this.email;
	}
	
	// Name related
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return this.name;
	}
	
	// Name related
		public void setId(String id) {
			this.idNumber = id;
		}
		public String getId() {
			return this.idNumber;
		}
}
